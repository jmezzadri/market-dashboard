// Map every registry entry into a single lowercase searchable blob so a
// top-of-page search input can filter tiles with a simple substring test.
export function buildSearchBlob(row) {
  const parts = [
    row.name, row.longName, row.source, row.seriesId, row.freq,
    row.summary, row.details,
    (row.powers||[]).join(" "),
    (row.keywords||[]).join(" "),
    row.key,
  ];
  return parts.filter(Boolean).join(" | ").toLowerCase();
}
